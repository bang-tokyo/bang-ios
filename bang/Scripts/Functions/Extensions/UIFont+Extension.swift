extension UIFont {
    class func hirakakuProNW3(size: CGFloat) -> UIFont? {
        return UIFont(name: "HiraKakuProN-W3", size: size)
    }

    class func hirakakuProNW6(size: CGFloat) -> UIFont? {
        return UIFont(name: "HiraKakuProN-W6", size: size)
    }
}
